package StarPatters_03;

public class Main 
{
	//***
	//***
		
		
		public static void main(String[] args) 
		{
			
			for(int i=1; i<=2; i++)	    //outer for loop --> Rows
			{
				for(int j=1; j<=3; j++)	//inner for loop --> Columns
				{
					System.out.print("*");
				}
				
				System.out.println();
				
			}
			
			
			
		}



}
