package All_Logical_Programs;

public class Verify_that_the_Number_is_Even_or_Odd_03 
{
	public static void main(String[]args)
	{
		int NUM = 1;
		
		if(NUM%2==0)
		{
			System.out.println(" Given Number is EVEN");
		}
		else
		{
			System.out.println(" Given Number is ODD");
		}
	}
	

}
