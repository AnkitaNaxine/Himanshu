package All_Logical_Programs;

public class Multiplication_of_Two_Numbers_Without_Multiplication_Operator_02
{
	public static void main(String[]args)
	{
		int NUM1 = 19;
		int NUM2 = 5;
		int SUM = 0;
		
		for(int i=1; i<=NUM2; i++)
		{
			SUM = SUM+NUM1;
		}
		System.out.println(SUM);
	}

}
