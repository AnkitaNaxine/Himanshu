package Multiplication_of_two_Numbers_without_Multiplication_Operator_02;

public class Practice05 
{
	public static void main(String[]args) 
	{
		System.out.println(" Multiply Two Numbers without using Multiplication Operation ");
	
	int A=120;
	int B=4;
	
	int sum=0;
	
	for(int i=1; i<=B; i++)
	{
		sum=sum+A;
	}
	
	System.out.println(sum);
	
	}

}
