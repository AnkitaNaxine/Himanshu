package Multiplication_of_two_Numbers_without_Multiplication_Operator_02;

public class Practice11 
{
	public static void main(String[]args)
	{
		int NUM1 = 10;
		int NUM2 = 12;
		
		int SUM = 0;
		
		for(int i=1; i<=NUM1; i++)
		{
			SUM = SUM+NUM2;
		}
		
		System.out.println(SUM);
	}

}
