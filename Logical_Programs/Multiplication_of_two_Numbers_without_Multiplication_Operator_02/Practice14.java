package Multiplication_of_two_Numbers_without_Multiplication_Operator_02;

public class Practice14 
{
	public static void main(String[]args)
	{
		int NUM1 = 24;
		int NUM2 = 4;
		int SUM = 0;
		
		for(int i=1; i<=NUM2; i++)
		{
			SUM = SUM+NUM1;
		}
		
		System.out.println(SUM);
		
	}

}
