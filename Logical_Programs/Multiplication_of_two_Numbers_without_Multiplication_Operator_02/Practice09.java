package Multiplication_of_two_Numbers_without_Multiplication_Operator_02;


public class Practice09 
{
	public static void main(String[]args) 
	{
		System.out.println(" Perform Multiplication Operation Without Multiply Symbol ");
	
	
	int NUM1 = 700;
	int NUM2 = 4;
	
	int SUM=0;
	
	for(int i=1; i<=NUM2; i++) 
	{
		SUM = SUM+NUM1;
	}
	
	System.out.println(SUM);
	
	}

}
