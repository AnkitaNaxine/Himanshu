package Verify_Num_is_Even_Odd_03;

public class Practice07 
{
	public static void main(String[]args)
	{
		int NUM = 1;
		
		if(NUM%2==0)
		{
			System.out.println(" Given Number "+NUM+" is EVEN");
		}
		else
		{
			System.out.println(" Given Number "+NUM+" is ODD");
		}
		
		
	}

}
